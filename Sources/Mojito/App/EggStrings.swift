import Foundation

/// Trigger keywords for the hidden effects, encoded so they don't appear as
/// plaintext in the source. The pre-discovery hints and the SHA-256 lookup
/// table in `EggIndex` were already keyword-free; this closes the last leak —
/// the post-discovery "detail" lines in `EasterEgg.detail`, which used to
/// hard-code strings like `:mojito:` and let any GitHub reader grep them out.
///
/// Encoding: bytewise XOR against a rolling key starting at `0xA5`, advancing
/// `+1` per byte. Trivially reversible to anyone determined enough, but it
/// defeats `strings <binary>` and a casual scan of the source. Regenerate
/// arrays with `python3 scripts/encode_triggers.py` (script lives in the
/// repo only as the recipe in `EggStrings.swift`'s history — kept out of
/// Resources on purpose, since shipping the plaintext alongside the cipher
/// would defeat the point).
enum EggStrings {
    private static func decode(_ bytes: [UInt8]) -> String {
        var out = [UInt8]()
        out.reserveCapacity(bytes.count)
        for (i, b) in bytes.enumerated() {
            out.append(b ^ UInt8((0xA5 &+ i) & 0xFF))
        }
        return String(decoding: out, as: UTF8.self)
    }

    // Post-discovery trigger reveals. Each string is the full `:keyword:`
    // form that appears in `EasterEgg.detail`. Generated, do not hand-edit.
    static let mojito: String        = decode([0x9f, 0xcb, 0xc8, 0xc2, 0xc0, 0xde, 0xc4, 0x96])
    static let moof: String          = decode([0x9f, 0xcb, 0xc8, 0xc7, 0xcf, 0x90])
    static let confetti: String      = decode([0x9f, 0xc5, 0xc8, 0xc6, 0xcf, 0xcf, 0xdf, 0xd8, 0xc4, 0x94])
    static let pride: String         = decode([0x9f, 0xd6, 0xd5, 0xc1, 0xcd, 0xcf, 0x91])
    static let sosumi: String        = decode([0x9f, 0xd5, 0xc8, 0xdb, 0xdc, 0xc7, 0xc2, 0x96])
    static let floppy: String        = decode([0x9f, 0xc0, 0xcb, 0xc7, 0xd9, 0xda, 0xd2, 0x96])
    static let dialup: String        = decode([0x9f, 0xc2, 0xce, 0xc9, 0xc5, 0xdf, 0xdb, 0x96])
    static let wilhelm: String       = decode([0x9f, 0xd1, 0xce, 0xc4, 0xc1, 0xcf, 0xc7, 0xc1, 0x97])
    static let snow: String          = decode([0x9f, 0xd5, 0xc9, 0xc7, 0xde, 0x90])
    static let matrix: String        = decode([0x9f, 0xcb, 0xc6, 0xdc, 0xdb, 0xc3, 0xd3, 0x96])
    static let fireworks: String     = decode([0x9f, 0xc0, 0xce, 0xda, 0xcc, 0xdd, 0xc4, 0xde, 0xc6, 0xdd, 0x95])
    static let trogdor: String       = decode([0x9f, 0xd2, 0xd5, 0xc7, 0xce, 0xce, 0xc4, 0xde, 0x97])
    static let lost: String          = decode([0x9f, 0xca, 0xc8, 0xdb, 0xdd, 0x90])
    static let toasters: String      = decode([0x9f, 0xd2, 0xc8, 0xc9, 0xda, 0xde, 0xce, 0xde, 0xde, 0x94])
    static let dvd: String           = decode([0x9f, 0xc2, 0xd1, 0xcc, 0x9b, 0x90])
    static let bsod: String          = decode([0x9f, 0xc4, 0xd4, 0xc7, 0xcd, 0x90])
    static let snake: String         = decode([0x9f, 0xd5, 0xc9, 0xc9, 0xc2, 0xcf, 0xcc, 0xcd, 0xc0, 0xcb, 0x95])
    static let thermonuclear: String = decode([0x9f, 0xc1, 0xcb, 0xc7, 0xcb, 0xcb, 0xc7, 0xd8, 0xc5, 0xcb, 0xdd, 0xdd, 0xde, 0xdc, 0xc6, 0xd7, 0xd9, 0xd3, 0xd6, 0xca, 0xce, 0xdb, 0xc9, 0x86])
    static let myleg: String         = decode([0x9f, 0xcb, 0xde, 0xc4, 0xcc, 0xcd, 0x91])
    static let tada: String          = decode([0x9f, 0xd2, 0xc6, 0xcc, 0xc8, 0x90])
    static let xp: String            = decode([0x9f, 0xde, 0xd7, 0x92])
    static let solitaire: String     = decode([0x9f, 0xd5, 0xc8, 0xc4, 0xc0, 0xde, 0xca, 0xc5, 0xdf, 0xcb, 0x95])
    static let rickroll: String      = decode([0x9f, 0xd4, 0xce, 0xcb, 0xc2, 0xd8, 0xc4, 0xc0, 0xc1, 0x94])
    static let crt: String           = decode([0x9f, 0xc5, 0xd5, 0xdc, 0x93])
    static let celery: String        = decode([0x9f, 0xc5, 0xc2, 0xc4, 0xcc, 0xd8, 0xd2, 0x96])

    // Picker pinned-row labels (no surrounding colons).
    static let confettiLabel: String = decode([0xc6, 0xc9, 0xc9, 0xce, 0xcc, 0xde, 0xdf, 0xc5])
    static let prideLabel: String    = decode([0xd5, 0xd4, 0xce, 0xcc, 0xcc])
}
